    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        if (txtNo.getText().isEmpty() ||
            txtTanggal.getText().isEmpty() ||
            txtWaktu.getText().isEmpty() ||
            txtareakegiatan.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
            return;
        }

        try {
            LocalDate tanggal = LocalDate.parse(
                txtTanggal.getText(), formatter
            );

            DailyPlan dp = new DailyPlan(
            txtNo.getText(),
            tanggal,
            txtWaktu.getText(),
            txtareakegiatan.getText()
            );

            controller.save(dp);
            clearForm();
            updateNoOtomatis();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Format tanggal harus dd-MM-yyyy");
        }
    }//GEN-LAST:event_btnsaveActionPerformed

