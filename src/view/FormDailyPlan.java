    private void btnsetAlarmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsetAlarmActionPerformed
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!");
            return;
        }

        LocalDate tanggal = LocalDate.parse(
            model.getValueAt(row, 1).toString(),
            DateTimeFormatter.ofPattern("dd-MM-yyyy")
        );

        String waktuStr = model.getValueAt(row, 2).toString();
        int hour = Integer.parseInt(waktuStr.substring(0, 2));
        int minute = Integer.parseInt(waktuStr.substring(3, 5));

        FormAlarm fa = new FormAlarm();
        fa.tambahAlarmDariDailyPlan(tanggal, hour, minute);
        fa.setVisible(true);
    }//GEN-LAST:event_btnsetAlarmActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if (row < 0) return;

        txtNo.setText(model.getValueAt(row, 0).toString());
        txtTanggal.setText(model.getValueAt(row, 1).toString());
        txtWaktu.setText(model.getValueAt(row, 2).toString());
        txtareakegiatan.setText(model.getValueAt(row, 3).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        int row = jTable1.getSelectedRow();

        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diupdate!");
            return;
        }

        try {
            LocalDate tanggal = LocalDate.parse(txtTanggal.getText(), formatter);

            DailyPlan dp = new DailyPlan(
                txtNo.getText(),
                tanggal,
                txtWaktu.getText(),
                txtareakegiatan.getText()
            );

            controller.update(row, dp);
            clearForm();
            updateNoOtomatis();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Format tanggal salah!");
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int row = jTable1.getSelectedRow();

        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Yakin ingin menghapus data ini?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            controller.delete(row);
            clearForm();
            updateNoOtomatis();
        }
    }//GEN-LAST:event_btndeleteActionPerformed

