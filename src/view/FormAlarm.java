    private void removebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removebtnActionPerformed
    int index = listalarm.getSelectedIndex();
    if (index == -1) {
        JOptionPane.showMessageDialog(this, "Pilih alarm!");
        return;
    }
    controller.removeAlarm(index);
    }//GEN-LAST:event_removebtnActionPerformed

    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
    String nada = comboNada.getSelectedItem().toString();
    if (nada.equals("-- PILIH NADA --")) {
        JOptionPane.showMessageDialog(this, "Pilih nada dulu!");
        return;
    }

    Date d = (Date) spindate.getValue();
    LocalDate tanggal = d.toInstant()
        .atZone(ZoneId.systemDefault())
        .toLocalDate();

    Date t = (Date) spintime.getValue();
    LocalTime waktu = t.toInstant()
        .atZone(ZoneId.systemDefault())
        .toLocalTime();

    controller.setAlarm(
        tanggal,
        waktu.getHour(),
        waktu.getMinute(),
        nada
    );
    }//GEN-LAST:event_addbtnActionPerformed

