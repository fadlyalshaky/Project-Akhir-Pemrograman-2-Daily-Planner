public void tambahAlarmDariDailyPlan(LocalDate tanggal, int hour, int minute) {

    lblTanggalAlarm.setText(
        tanggal.getDayOfWeek() + ", " +
        tanggal.format(
            java.time.format.DateTimeFormatter.ofPattern("dd-MM-yyyy")
        )
    );

    Date date = Date.from(
        tanggal.atStartOfDay(ZoneId.systemDefault()).toInstant()
    );
    spindate.setValue(date);

    java.util.Calendar cal = java.util.Calendar.getInstance();
    cal.setTime((Date) spintime.getValue());
    cal.set(java.util.Calendar.HOUR_OF_DAY, hour);
    cal.set(java.util.Calendar.MINUTE, minute);
    spintime.setValue(cal.getTime());
}
